/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
    volatile uint32_t rng;
    volatile uint8_t rng_byte;

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();

  /* Initialize all configured peripherals */
//  MX_GPIO_Init();
  MX_RNG_Init();
//  MX_TIM6_Init();

  buffer_init(&m_buffer, buff_array, (uint16_t)BUFFER_SIZE, uart_send_handler);
  uart_init(uart_init_handler);

  while (1)
  {
      rng = HAL_RNG_GetRandomNumber(&hrng);
      rng_byte = rng & MSK;

      put_byte_to_buffer(rng_byte, &m_buffer);
      HAL_Delay(rng_byte);
  }
}

/* Функция отправки байта данных через UART */
void uart_send_handler(uint8_t databyte)
{
    uint8_t data = databyte;
    HAL_UART_Transmit_IT(&huart1, &data, 1);
}

/* Функция инициализации UART */
void uart_init_handler(uart_init_t uart_param)
{
  huart1.Instance = USART1;
  huart1.Init.BaudRate = uart_param.baudrate;
  if(uart_param.worldlenght == M_DATA_LENGTH_8BIT){
      huart1.Init.WordLength = UART_WORDLENGTH_8B;
  } else{
      huart1.Init.WordLength = UART_WORDLENGTH_9B;
  }
  if(uart_param.stopbits == M_UART_STOPBITS_1){
      huart1.Init.StopBits = UART_STOPBITS_1;
  }else{
      huart1.Init.StopBits = UART_STOPBITS_2;
  }
  if(uart_param.parity == M_UART_PARITY_NONE){
      huart1.Init.Parity = UART_PARITY_NONE;
  }else{
      huart1.Init.Parity = UART_PARITY_EVEN;
  }
  huart1.Init.Mode = UART_MODE_TX;
  huart1.Init.HwFlowCtl = UART_HWCONTROL_NONE;
  huart1.Init.OverSampling = UART_OVERSAMPLING_16;
  if (HAL_UART_Init(&huart1) != HAL_OK)
  {
    Error_Handler();
  }
}

